/*
    Write a function that accepts a base and an exponent.
    It should return the power of the base to the exponent.
    power(2, 0) //returns 1
    power(2, 2) //returns 3
    power(2, 4) //returns 16 
*/

function power(base, exponent)  {
    return Math.pow(base, exponent)
}

